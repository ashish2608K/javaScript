// What is an Index?
// Simple Definition

// An index is the position of an element inside an array.

// Imagine a row of lockers:

// ┌──────────┬──────────┬──────────┬──────────┐
// │Barcelona │Liverpool │ Arsenal  │ Chelsea  │
// └──────────┴──────────┴──────────┴──────────┘
//      0           1          2          3

// Notice something?

// 👉 JavaScript starts counting from 0, not 1.

// This is called Zero-Based Indexing.


// Example
// const footballClubs = [
//     "Barcelona",
//     "Liverpool",
//     "Arsenal",
//     "Chelsea"
// ];

// The indexes are:

// Value          Index

// Barcelona   →   0
// Liverpool   →   1
// Arsenal     →   2
// Chelsea     →   3

// Accessing Values                                                      
// We use:
// arrayName[index]

// Example:
